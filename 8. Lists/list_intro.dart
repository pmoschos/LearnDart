void main() {
  // List of integers
  List<int> scores = [85, 92, 78];

  // List of strings
  List<String> colors = ["red", "blue", "green"];

  // Mixed List
  var info = [23, "Alex", true];

  print(scores); // Output: [85, 92, 78]
  print(colors); // Output: [red, blue, green]
  print(info); // Output: [23, Alex, true]
}
