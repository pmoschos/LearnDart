void main() {
  // Integer List
  List<int> ages = [10, 30, 23];

  // String List
  List<String> names = ["Raj", "John", "Rocky"];

  // Mixed List
  var mixed = [10, "John", 18.8];

  print(ages); // Output: [10, 30, 23]
  print(names); // Output: [Raj, John, Rocky]
  print(mixed); // Output: [10, John, 18.8]
}
